
def day_week(num):
    if num == 0:
        return "Понедельник"
    elif num == 1:
        return "Вторник"
    elif num == 2:
        return "Среда"
    elif num == 3:
        return "Четверг"
    elif num == 4:
        return "Пятница"
    elif num == 5:
        return "Суббота"
    elif num == 6:
        return "Воскресенье"